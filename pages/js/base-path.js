// GitHub Pages для проектного сайта отдаёт его из подпапки (/имя-репозитория/),
// а не из корня домена. Локально (python -m http.server) и на будущем кастомном
// домене подпапки нет. Эта функция вычисляет разницу один раз и добавляет её
// перед корне-абсолютными путями ("/content/...", "/pages/...") — но ТОЛЬКО в
// местах, где путь реально используется несколькими страницами на разной
// глубине вложенности (layout.js, содержимое модулей). Для ссылок внутри одной
// конкретной страницы на файлы фиксированной глубины проще и надёжнее обычные
// относительные пути ("../design/tokens.css?v=5") — их это не касается.
export const BASE_PATH = location.hostname.endsWith("github.io")
  ? "/" + (location.pathname.split("/").filter(Boolean)[0] || "")
  : "";

export function withBase(rootRelativePath) {
  return BASE_PATH + rootRelativePath;
}
